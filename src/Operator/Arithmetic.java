package Operator;

public class Arithmetic {
    public static void main(String[] args) {

        //arithmetic operators
        //return type is value
        int a = 2;
        int b = 4;
        System.out.println(a+b);
        System.out.println(2/3);
        System.out.println(2*3);
        System.out.println(2%1);
        System.out.println(4-2);
    }
}
